export const analyzeOffline = (text: string): string => {
    const lower = text.toLowerCase();

    // 1. Safety First (Critical)
    if (lower.match(/(suicid|kill myself|want to die|hurt myself|end it all|overdose|cutting)/)) {
        return offlineResponses.crisis[0];
    }

    // 2. Complex Keyword Matching
    if (lower.match(/(hi|hello|hey|yo|start|morning|afternoon|evening)/)) return getRandom(offlineResponses.greeting);

    if (lower.match(/(panic|anxiet|scared|afraid|terrified|heart|breathe|shaking|nervous)/)) return getRandom(offlineResponses.anxiety);

    if (lower.match(/(exam|grade|study|school|fail|assignment|homework|class|teacher|gpa|test)/)) return getRandom(offlineResponses.academic);

    if (lower.match(/(sleep|tired|awake|insomnia|night|dream|exhausted|bed)/)) return getRandom(offlineResponses.sleep);

    if (lower.match(/(lonely|alone|friend|isolate|ignored|nobody|disconnect)/)) return getRandom(offlineResponses.lonely);

    if (lower.match(/(stress|worry|panel|work|deadlines|hard|tough|overwhelm|pressure|busy)/)) return getRandom(offlineResponses.stress);

    if (lower.match(/(sad|cry|depressed|down|bad|hurt|pain|heavy|dark|grief|miss)/)) return getRandom(offlineResponses.sad);

    if (lower.match(/(happy|great|good|win|excited|proud|best|yay|awesome|love|like)/)) return getRandom(offlineResponses.happy);

    // 3. Default Fallback
    return getRandom(offlineResponses.default);
};

const getRandom = (arr: string[]) => arr[Math.floor(Math.random() * arr.length)];
